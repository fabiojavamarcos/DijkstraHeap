package main;

public class No {
	private int num;
	private int estimativa;
	
	public No(int num, int estimativa) {
		super();
		this.num = num;
		this.estimativa = estimativa;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public int getEstimativa() {
		return estimativa;
	}
	public void setEstimativa(int estimativa) {
		this.estimativa = estimativa;
	}
}
